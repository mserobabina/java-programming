package day03_escape_sequence;

public class EscapeSequence2 {
    public static void main(String[] args){
        System.out.println("\"iphone\",\n \"xbox\", \t\"computer\", \"monitor\",\\.");
        System.out.println();
        System.out.println("------------------------");
        System.out.println();

        System.out.println("      ^       ");
        System.out.println();
        System.out.println("   /     \\    ");
        System.out.println("  |       |");
        System.out.println("  ---------");

    }
}
