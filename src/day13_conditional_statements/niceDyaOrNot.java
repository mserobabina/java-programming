package day13_conditional_statements;

public class niceDyaOrNot {
    public static void main(String[] args) {
        int temp = 60;
        if(temp>=70) {
            System.out.println("It is beautiful day!!!");
            System.out.println("Please, go outside and enjoy the weather!!!");
        }else{
            System.out.println("Oh...It is way too cold!!!");
            System.out.println("Please, stay home!!!");
        }
    }
}
