package Day29_nestedLoops;

public class IntoToArrays {
    public static void main(String[] args) {
        int num=10;
        int [] nums = new int[3];
        nums[0]=3;
        nums[1]=5;
        nums[2]=7;
        System.out.println(nums[0]);
        System.out.println(nums[1]);
        System.out.println(nums[2]);
        System.out.println("-------------------");


        int i=0;
        System.out.println(nums[i]);
        i++;
        System.out.println(nums[i]);
    }
}
