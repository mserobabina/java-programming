package day11_comrarison_operators;

public class BooleanComparisonOperators {
    public static void main(String[] args) {
        int a = 100;
        int b = 200;
        System.out.println(a==b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a<=b);
        System.out.println(a>=b);
        System.out.println(a!=b);

        boolean result = 5==5;
        System.out.println(result);
        result = 15<=10;
        System.out.println(result);
    }
}
